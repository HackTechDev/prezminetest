<section>
    <h3>Principe du jeu</h3>
    <p>
Il s’agit d’un jeu d’aventure dans lequel les joueurs peuvent construire des bâtiments à partir de petits blocs cubiques.
Un monde fait de cubes : terre, eau, sable, minéraux, faune et flore… dont les règles qui le régissent simulent le nôtre (jour & nuit, température, érosion, reproduction des espèces…)
	</p>
</section>

