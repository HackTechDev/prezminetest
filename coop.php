    <p>
Coopératif : Permet à plusieurs utilisateurs d’atteindre ensemble un même objectif 
Chacun apporte sa pierre à l’édifice final, sans toucher/modifier la construction de son ou ses partenaires.
<br/>
<br/>
Collaboratif : Permet à plusieurs utilisateurs d’atteindre ce même objectif
Chacun d’apporter sa pierre à l’édifice final et de modifier, supprimer celle de son et ses partenaires.
	</p>
</section>

